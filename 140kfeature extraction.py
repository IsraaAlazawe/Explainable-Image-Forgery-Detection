import time
import pandas as pd

from sklearn.ensemble import RandomForestClassifier, ExtraTreesClassifier, StackingClassifier, VotingClassifier
from sklearn.svm import SVC
from sklearn.linear_model import LogisticRegression

from xgboost import XGBClassifier
from lightgbm import LGBMClassifier
from catboost import CatBoostClassifier

# ==============================
# تعريف النماذج
# ==============================
models = {
    "LightGBM": LGBMClassifier(n_estimators=300),
    "RandomForest": RandomForestClassifier(n_estimators=200),
    "ExtraTrees": ExtraTreesClassifier(n_estimators=200),
    "XGBoost": XGBClassifier(eval_metric='logloss'),
    "CatBoost": CatBoostClassifier(verbose=0),
    "SVM": SVC(probability=True)
}

# ==============================
# Stacking
# ==============================
stack_model = StackingClassifier(
    estimators=[
        ('lgbm', LGBMClassifier(n_estimators=200)),
        ('rf', RandomForestClassifier(n_estimators=200)),
        ('xgb', XGBClassifier(eval_metric='logloss')),
        ('extra', ExtraTreesClassifier(n_estimators=200)),
        ('cat', CatBoostClassifier(verbose=0))
    ],
    final_estimator=LogisticRegression()
)

# ==============================
# Voting
# ==============================
voting_model = VotingClassifier(
    estimators=[
        ('lgbm', LGBMClassifier(n_estimators=200)),
        ('rf', RandomForestClassifier(n_estimators=200)),
        ('xgb', XGBClassifier(eval_metric='logloss')),
        ('extra', ExtraTreesClassifier(n_estimators=200)),
        ('cat', CatBoostClassifier(verbose=0))
    ],
    voting='soft'
)

# ==============================
# إضافة للـ dictionary
# ==============================
models["Stacking"] = stack_model
models["Voting"] = voting_model

# ==============================
# تشغيل كل النماذج
# ==============================
results = []

for name, m in models.items():

    # ⏱ Training Time
    start_train = time.time()
    m.fit(X_train, y_train)
    end_train = time.time()
    train_time = end_train - start_train

    # ⏱ Testing Time
    start_test = time.time()
    pred  = m.predict(X_test)
    proba = m.predict_proba(X_test)[:,1]
    end_test = time.time()
    test_time = end_test - start_test

    # حفظ النتائج
    results.append([
        name,
        accuracy_score(y_test, pred),
        roc_auc_score(y_test, proba),
        train_time,
        test_time,
        test_time / len(X_test)
    ])

# ==============================
# إنشاء الجدول
# ==============================
df = pd.DataFrame(results, columns=[
    "Model",
    "Accuracy",
    "AUC",
    "Training Time (s)",
    "Testing Time (s)",
    "Time per Sample (s)"
])

