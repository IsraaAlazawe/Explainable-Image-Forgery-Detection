from tensorflow.keras.applications import ResNet50
from tensorflow.keras.applications.resnet50 import preprocess_input

resnet = ResNet50(
    weights='imagenet',
    include_top=False,
    input_shape=(IMG_SIZE_RGB, IMG_SIZE_RGB, 3)
)

resnet.trainable = False

# Block 7 — استخراج الميزات
map_model = build_map_cnn()

def extract_features(dataset):

    all_features = []
    all_labels = []

    for (maps, rgbs), labels in dataset:

        # CNN features (maps)
        f1 = map_model(maps, training=False)

        # ResNet features (rgb)
        rgbs_resnet = preprocess_input(rgbs * 255.0)
        f2 = resnet(rgbs_resnet, training=False)
        f2 = tf.keras.layers.GlobalAveragePooling2D()(f2)

        # Normalization (سريع)
        f1 = (f1 - tf.reduce_mean(f1)) / (tf.math.reduce_std(f1) + 1e-8)
        f2 = (f2 - tf.reduce_mean(f2)) / (tf.math.reduce_std(f2) + 1e-8)

        # Fusion
        fused = tf.concat([f1, f2], axis=1)

        all_features.append(fused.numpy())
        all_labels.append(labels.numpy())

        print("Processed batch")

    return np.vstack(all_features), np.concatenate(all_labels)

