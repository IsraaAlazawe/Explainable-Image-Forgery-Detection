def find_rgb_image(rgb_folder, name):
    exts = ['.jpg','.png','.jpeg','.tif','.tiff','.bmp']
    for ext in exts:
        p = os.path.join(rgb_folder, name + ext)
        if os.path.exists(p):
            return p
    return None


