def build_dataset(data):

    print("Total samples:", len(data))

    map_paths = [d[0] for d in data]
    rgb_paths = [d[1] for d in data]
    labels    = [d[2] for d in data]

    dataset = tf.data.Dataset.from_tensor_slices((map_paths, rgb_paths, labels))

    dataset = dataset.map(
        lambda m, r, y: parse_function(m, r, y),
        num_parallel_calls=tf.data.AUTOTUNE
    )

    dataset = dataset.batch(BATCH_SIZE)
    dataset = dataset.prefetch(tf.data.AUTOTUNE)

    return dataset

