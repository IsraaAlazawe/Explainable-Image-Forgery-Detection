def load_map_cv2(path):

    path = path.numpy().decode()

    img = cv2.imread(path, cv2.IMREAD_UNCHANGED)

    if img is None:
        print("⚠️ Missing MAP:", path)
        img = np.zeros((IMG_SIZE_MAP, IMG_SIZE_MAP), dtype=np.uint8)

    if len(img.shape) == 2:
        img = cv2.cvtColor(img, cv2.COLOR_GRAY2RGB)

    img = cv2.resize(img, (IMG_SIZE_MAP, IMG_SIZE_MAP))
    img = img.astype(np.float32) / 255.0

    return img


# ==========================================
# قراءة صور RGB
# ==========================================
def load_rgb_cv2(path):

    path = path.numpy().decode()

    img = cv2.imread(path)

    if img is None:
        print("⚠️ Missing RGB:", path)
        img = np.zeros((IMG_SIZE_RGB, IMG_SIZE_RGB, 3), dtype=np.uint8)

    img = cv2.resize(img, (IMG_SIZE_RGB, IMG_SIZE_RGB))
    img = img.astype(np.float32) / 255.0

    return img


# ==========================================
# parse_function
# ==========================================
def parse_function(map_path, rgb_path, label):

    map_img = tf.py_function(load_map_cv2, [map_path], tf.float32)
    map_img.set_shape((IMG_SIZE_MAP, IMG_SIZE_MAP, 3))

    rgb_img = tf.py_function(load_rgb_cv2, [rgb_path], tf.float32)
    rgb_img.set_shape((IMG_SIZE_RGB, IMG_SIZE_RGB, 3))

    return (map_img, rgb_img), label

# ==========================================
# build_dataset
