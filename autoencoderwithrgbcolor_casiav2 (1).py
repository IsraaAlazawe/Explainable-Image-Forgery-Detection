    model.compile(optimizer=optimizers.Adam(learning_rate=1e-4), loss='mae')

    # الحفاظ على أفضل نسخة من الموديل
    checkpoint = callbacks.ModelCheckpoint(save_model_path, save_best_only=True, monitor='loss')

