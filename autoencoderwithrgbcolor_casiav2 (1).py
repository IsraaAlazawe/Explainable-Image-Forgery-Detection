# دالة المعالجة
# ==========================================
def process_image(img_path, category):
    img_name = os.path.basename(img_path)
    base_name = os.path.splitext(img_name)[0]

    # اسم جديد للماسك فقط
    mask_name = f"{category}_{base_name}"

    # قراءة الصورة
    img = cv2.imread(img_path)
    if img is None:
        return

    img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    img_resized = cv2.resize(img_rgb, (384, 256))
    img_input = np.expand_dims(img_resized / 255.0, axis=0)

    # التنبؤ
    reconstructed = model.predict(img_input, verbose=0)[0]
    original = img_input[0]

    # Residual
    residual = np.mean(np.abs(original - reconstructed), axis=-1)

    # ==========================================
    # حفظ TIFF (maps)
    # ==========================================
    if category == 'Au':
        tiff_path = os.path.join(maps_au_dir, f"{base_name}.tiff")
    else:
        tiff_path = os.path.join(maps_tp_dir, f"{base_name}.tiff")

    cv2.imwrite(tiff_path, residual.astype(np.float32))

    # ==========================================
    # إنشاء mask
    # ==========================================
    blurred = cv2.GaussianBlur(residual, (5, 5), 0)

    max_val = np.max(blurred)
    if max_val > 0:
        enhanced = (blurred / max_val * 255).astype(np.uint8)
    else:
        enhanced = np.zeros_like(blurred, dtype=np.uint8)

    # Threshold تكيفي
    thresh_val = np.percentile(enhanced, 97)
    _, binary_mask = cv2.threshold(enhanced, thresh_val, 255, cv2.THRESH_BINARY)

    # تنظيف
    kernel = np.ones((3, 3), np.uint8)
    binary_mask = cv2.morphologyEx(binary_mask, cv2.MORPH_OPEN, kernel)

    # ==========================================
    # حفظ mask (في مجلد واحد)
    # ==========================================
    mask_path = os.path.join(masks_dir, f"{mask_name}.png")
    cv2.imwrite(mask_path, binary_mask)


